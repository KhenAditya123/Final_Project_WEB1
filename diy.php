    <p>Sate klatak is a unique goat or mutton satay dish, originally from Pleret District, Bantul Regency in
      Yogyakarta.In Javanese, the act of roasting satay in an open fire is called "klathak".
<br>
      This satay is quite different from other variants of satay, in that it uses mainly salt and a pinch of pepper as
      its main marinating seasoning. The skewers used to grill or roast the satay are made from iron, unlike other
      satays which use bamboo skewers. The iron skewers act as heat conductors and help the meat cook evenly from the
      inside.
<br>
      The satay is usually served with gulai (curry soup). The gulai is richly spiced, sometimes cooked with lamb
      bone, and boiled using a small fire for approx. 30 minutes.</p>

