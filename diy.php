    <p>Gudeg is a traditional Javanese dish from Yogyakarta and Central Java, Indonesia. Gudeg is made from young unripe
      jack fruit (Javanese: gori, Indonesian: nangka muda) stewed for several hours with palm sugar, and coconut
      milk. Additional spices include garlic, shallot, candlenut, coriander seed, galangal, bay leaves, and teak
      leaves, the latter giving a reddish-brown color to the dish. It is often described as "green jack fruit sweet
      stew".</p>

