    <p>Wedang uwuh is a drink with ingredients in the form of leaves similar to spices. In Javanese, wedang means brewed
      drink, while uwuh means trash. Wedang uwuh served hot or warm has a sweet and spicy taste with a bright red color
      and fragrant aroma. The spicy taste is due to the ginger, while the red color is due to the presence of secang.
      Wedang uwuh is a typical drink from Yogyakarta. At first Wedang Uwuh was still in the form of whole ingredients in
      the form of original spices, but along with the times and the need for practicality, now Wedang Uwuh has been
      developed into instant form, as well as dip form. Along with its development, now Wedang Uwuh is not just a
      typical drink. But Wedang Uwuh has been crowned as an intangible heritage that has such great value.</p>

