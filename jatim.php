        <p>Tahu campur, literally meaning "mixed tofu" in Javanese language and broader Indonesian language, is an East
            Javanese tofu dish. The dish consists of sliced tahu goreng (fried tofu), lontong (rice cakes), lentho
            (fried black-eyed pea patty) or sometimes replaced by perkedel (potato or cassava patty cakes), fresh bean
            sprouts, fresh lettuce, yellow noodles, and krupuk crackers, served in savoury beef stew, garnished with
            fried onions, and sambal chili sauce.The beef stew soup is seasoned with spices and petis, a type of
            shrimp paste commonly used in East Javanese cuisine.
            <br>
            The dish is associated with Surabaya metropolitan area (Gerbangkertosusila), which includes Surabaya,
            Lamongan, Gresik, and Sidoarjo. This dish is commonly sold by street vendors in major Indonesian cities,
            especially in Java, with the tarp tent shop or warung usually called their establishments as "Tahu Campur
            Lamongan".
        </p>

