        <p>Rujak cingur is one of the traditional foods that is easy to find in East Java, especially in its hometown of
            Surabaya. In Javanese, the word cingur means "mouth", this refers to the sliced ​​beef mouth or snout which
            is boiled and mixed into the dish. Rujak cingur usually consists of slices of several types of fruit such as
            cucumber, collards (krai, which is a type of cucumber typical of East Java), yam, young mango, pineapple,
            kedondong, then added rice cake, tofu, tempeh, bendoyo, cingur, and vegetables such as sprouts/ bean
            sprouts, kale, and long beans. All of the ingredients were mixed with a sauce or condiment made from
            processed shrimp paste, a little boiled water to dilute, sugar/brown sugar, chili pepper, fried peanuts,
            fried onions, salt and bananas thinly sliced ​​green beans that are still young (bananas klutuk). All the
            sauces/seasonings are mixed by grinding, then topped with cingur. If without cingur, this rujak is called
            rujak ulek.</p>

