        <p>Rawon is an Indonesian dish in the form of a meat soup with black broth mixed with special spices using
            kluwek. Rawon, although known as a special dish from East Java (Arekan area), is also known by the people of
            Central Java to the east (Surakarta area).
            <br>
            The meat for rawon is generally beef cut into small pieces, mainly the brisket. The soup seasoning is very
            typical of Indonesia, namely a mixture of shallots, garlic, galangal (laos), coriander, candlenut,
            lemongrass, turmeric, chili, kluwek, salt, and vegetable oil. All these ingredients are mashed, then sautéed
            until fragrant. This seasoning mixture is then put in the meat stew broth together with the meat. Rawon's
            distinctive dark color comes from kluwek. Abroad, rawon is known as black soup.
        </p>

